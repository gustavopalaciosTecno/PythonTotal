"""
En python se usa la función print para imprimir en pantalla 
la información que deseamos mostrar
"""

print("Hola gente de Argentina")

# se puede imprimir con comillas simples también
print('Hola mundo')
# imprimir valores con comillas simples y dobles
print("hola, como estas 'Josefina'!")

# o invertir el orden de las comillas
print('hola, como estas "Josefina"!')

# o usar dobles comillas pero separarlas con paréntesis
print("hola, como estas \"Josefina\"")

# no es lo más óptimo, pero se pueden hacer operaciones dentro del print
print(100 + 50)
print(10.5 + 18.8)

# también podemos concatenar strings
print("hola" + " " + "mundo")
print("hola" + " " + "comunidad de " + "programadores")

# puedo agregar una barra invertida y la letra n para separar
print("Puedo hacer esta impresion\nPuedo hacer esta impresión")

# también puedo agregar una barra invertida y una letra t para separar los textos
print("esto es una separación de textos\ty esto es otra separación")

# mostrar barra invertida
print("Este signo \\ es una barra invertida")

# si coloco una sola barra, también se muestra
print("Este signo \ es una barra invertida")