nombre = "Leticia"
apellido = "Bredice"

# Cadenas literales - PRIMERA FORMA TE MOSTRAR LOS VALORES - esta es la mejor forma
print(f"mi nombre es: {nombre} y mi apellido es: {apellido}") 

# Función format - SEGUNDA FORMA DE MOSTRAR LOS VALORES
print("mi nombre es {} y mi apellido es: {}".format(nombre,apellido))

# Otra forma alternativa pero no muy efectiva
print("mi nombre es: ", nombre + " y mi apellido es: ", apellido)