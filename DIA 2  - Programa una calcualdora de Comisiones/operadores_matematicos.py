x = 25
y = 20
z = 12

print(f"{x} más {y} más {z} es igual a: {x+y+z}")
print(f"{x} menos {y} menos {z} es igual a: {x-y-z}")
print(f"{x} por {y} por {z} es igual a: {x*y*z}")
print(f"{x} dividido {y} dividido {z} es igual a: {x/y/z}")

# división entera
print(f"{x} dividido {y} dividido {z} es igual a: {x//y//z}")

# módulo
print(f"el módulo entre {x} y {y} es {x % y}")

# potencia
print(f"la potencia entre {x} y {y} es {x ** y}")

# elevado al cubo
print(f"el valor {x} elevado a la tercera potencia es: {x**3}")

# raiz cuadrada
print(f"el valor {x} elevado a la raiz cuadrada: {x**0.5}")
